package day16_loopContinue;

public class nestedLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("wooden spoon");

        }
        for (int i = 0; i < 5; i++) {
            System.out.println("wooden spoon");

        }
        for (int i = 0; i < 5; i++) {
            System.out.println("wooden spoon");

        }
        for (int i = 0; i < 5; i++) {
            System.out.println("wooden spoon");

        }

        System.out.println("--------------------------");

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.println("wooden spoon");

            }



        }
    }
}
