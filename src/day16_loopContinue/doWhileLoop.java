package day16_loopContinue;

public class doWhileLoop {
    public static void main(String[] args) {
        boolean condition = false;
        for (; condition;) {
            System.out.println("hello world");
        }
        System.out.println("-----------------------------------------------");

        while (condition){
            System.out.println("hello cydeo");
        }

        System.out.println("--------------------------------------");

        do{
            System.out.println("hello cydeo");
        }while (condition);















    }
}
