package day04_concatenation;

public class concatenation_vs_Addition {

    public static void main(String[] args) {

        System.out.println("32" + 1);// 321 concatenation
        System.out.println(7 + "1");// 71 concatenation
        System.out.println("32" + "2");// 322 concatenation

        System.out.println("----------------------------");

        System.out.println(12 + 2.5);// addition

        System.out.println("------------------------------");

        System.out.println("the value is " + true);// concatenation





    }
}
