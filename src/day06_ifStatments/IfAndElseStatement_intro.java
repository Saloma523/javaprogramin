package day06_ifStatments;

public class IfAndElseStatement_intro {

    public static void main(String[] args) {


        int age = 28;

        if(age >= 21){

            System.out.println("Eligible");
        }

        if(age < 21){

            System.out.println("Eligible");
        }

        System.out.println("------------------------------------------");

        if(age >= 21){

            System.out.println("Eligible");
        }else{
            System.out.println("Eligible");

        }


    }
}
